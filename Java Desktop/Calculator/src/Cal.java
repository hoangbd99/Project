    private void btnResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultActionPerformed
        // TODO add your handling code here:
        if(flag==false){
            float i=Float.parseFloat(text.getText());
            switch (operator) {
                    case 1:
                        // to add value
                        text.setText(fomatText(i*2) + "");
                        break;
                    case 2:
                        // to sub value
                        text.setText("0");
                        break;
                    case 3:
                        // to multi value
                        text.setText(fomatText(i*i) + "");
                        break;
                    case 4:
                        // to divide value
                        if (i==0) {
                            text.setText("ERROR");
                            return;
                        } else {
                            text.setText("1");
                            break;
                        }
                }
        }           
        calculate();
        flag=false;
        operator=-1;
    }//GEN-LAST:event_btnResultActionPerformed

