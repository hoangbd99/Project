        public void calculate() {
            if(flag==true){
            if (operator == -1) {
                firstNumber = Float.parseFloat(text.getText());
            } else {
                secondNumber =  Float.parseFloat(text.getText());
                switch (operator) {
                    case 1:
                        // to add value
                        firstNumber = firstNumber+secondNumber;
                        break;
                    case 2:
                        // to sub value
                        firstNumber = firstNumber-secondNumber;
                        break;
                    case 3:
                        // to multi value
                        firstNumber = firstNumber*secondNumber;
                        break;
                    case 4:
                        // to divide value
                        if (secondNumber==0) {
                            text.setText("ERROR");
                            return;
                        } else {
                            firstNumber = firstNumber/secondNumber;
                            break;
                        }
                     case 5:
                        firstNumber = firstNumber*secondNumber/100;
                        break;
                }
            }
            text.setText(fomatText(firstNumber) + "");
            }

    }
    public String fomatText(float check) {
        
        int i = (int) check;
        if ((check - i) == 0) {
            return String.valueOf(i);
        } else {
            return String.valueOf(check);
        }
    }

