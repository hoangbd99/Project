    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      DBContext context = new DBContext();

        int index = tblBookInfo.getSelectedRow();
        if (index != -1) {
            int book_id = (int) tblBookInfo.getModel().getValueAt(index, 0);
            Book book = context.getBook(book_id);
            int click = JOptionPane.showConfirmDialog(this, "Do you want to delete?", null, JOptionPane.YES_NO_OPTION);
            if (click == JOptionPane.YES_OPTION) {
                context.delete(book);
                loadDataToTable();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

