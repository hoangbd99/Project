     private void loadDataToTable() {
      
        ArrayList<Book> books = context.getBooks();

        Object[] columns = {"book_id", "book_title", "book_author", "book_publish_year", "book_category" ,"book_keyword","book_status"};
        Object[][] data = new Object[books.size()][columns.length];
        for (int i = 0; i < books.size(); i++) {
            entity.Book s = books.get(i);
            data[i][0] = s.getBook_id();
            data[i][1] = s.getBook_title();
            data[i][2] = s.getBook_author();
            data[i][3] = s.getBook_publish_year();
            data[i][4] = s.getBook_category();
            data[i][5] = s.getBook_keyword();
            data[i][6] = s.getBook_status();
            
            
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        tblBookInfo.setModel(model);

    }

