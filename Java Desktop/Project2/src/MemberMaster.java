    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int index = tblMember.getSelectedRow();
        if (index != -1) {
            int id = (int) tblMember.getModel().getValueAt(index, 0);
            //JOptionPane.showMessageDialog(this, ""+id);
            UpdateMember us = new UpdateMember(this, true, id);
            us.setVisible(true);
            
        }
        loadDataToTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
      DBContext context = new DBContext();

        int index = tblMember.getSelectedRow();
        if (index != -1) {
            int mem_id = (int) tblMember.getModel().getValueAt(index, 0);
            entity.Member member = context.getMember(mem_id);
            int click = JOptionPane.showConfirmDialog(this, "Do you want to delete?", null, JOptionPane.YES_NO_OPTION);
            if (click == JOptionPane.YES_OPTION) {
                context.delete(member);
                loadDataToTable();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

