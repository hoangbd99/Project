    private void loadDataToTable() {
        DBContext context = new DBContext();
        ArrayList<entity.Member> members = context.getMembers();

        Object[] columns = {"mem_id", "mem_name", "mem_address", "mem_mobile", "mem_registered_date" ,"mem_active"};
        Object[][] data = new Object[members.size()][columns.length];
        for (int i = 0; i < members.size(); i++) {
            entity.Member s = members.get(i);
            data[i][0] = s.getMem_id();
            data[i][1] = s.getMem_name();
            data[i][2] = s.getMem_address();
            data[i][3] = s.getMem_mobile();
            data[i][4] = s.getMem_registered_date();
            data[i][5] = s.isMem_active();
            
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        tblMember.setModel(model);

    }
    
    
