    private void BtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUpdateActionPerformed
        int index = tblUser.getSelectedRow();
        if (index != -1) {
            int id = (int) tblUser.getModel().getValueAt(index, 0);
            //JOptionPane.showMessageDialog(this, ""+id);
            UpdateUser us = new UpdateUser(this, true, id);
            us.setVisible(true);
            
        }
        loadDataToTable();
    }//GEN-LAST:event_BtnUpdateActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        // TODO add your handling code here:
        DBContext context = new DBContext();

        int index = tblUser.getSelectedRow();
        if (index != -1) {
            int id = (int) tblUser.getModel().getValueAt(index, 0);
            User user = context.getUser(id);
            int click = JOptionPane.showConfirmDialog(this, "Do you want to delete?", null, JOptionPane.YES_NO_OPTION);
            if (click == JOptionPane.YES_OPTION) {
                context.delete(user);
                loadDataToTable();
            }
        }
    }//GEN-LAST:event_BtnDeleteActionPerformed

