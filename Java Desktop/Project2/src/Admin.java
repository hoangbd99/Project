    private void loadDataToTable() {
        DBContext context = new DBContext();
        ArrayList<User> users = context.getUsers();

        Object[] columns = {"id", "user_name", "user_password", "user_type", "user_active"};
        Object[][] data = new Object[users.size()][columns.length];
        for (int i = 0; i < users.size(); i++) {
            User s = users.get(i);
            data[i][0] = s.getId();
            data[i][1] = s.getUser_name();
            data[i][2] = s.getUser_password();
            data[i][3] = s.getUser_type();
            data[i][4] = s.isUser_active();
        }
        DefaultTableModel model = new DefaultTableModel(data, columns);
        tblUser.setModel(model);

    }

